ALTER TABLE "user" ADD COLUMN IF NOT EXISTS is_platform_admin boolean NOT NULL DEFAULT false;
ALTER TABLE "user" DROP COLUMN IF EXISTS role;

CREATE TABLE IF NOT EXISTS "pg-drizzle_organization" (
  id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  name varchar(256) NOT NULL,
  type varchar(32) NOT NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz
);
CREATE INDEX IF NOT EXISTS organization_type_idx ON "pg-drizzle_organization" (type);

CREATE TABLE IF NOT EXISTS "pg-drizzle_organization_domain" (
  id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  organization_id integer NOT NULL REFERENCES "pg-drizzle_organization"(id) ON DELETE CASCADE,
  domain varchar(255) NOT NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT organization_domain_unique UNIQUE (domain)
);
CREATE INDEX IF NOT EXISTS organization_domain_org_idx ON "pg-drizzle_organization_domain" (organization_id);

CREATE TABLE IF NOT EXISTS "pg-drizzle_organization_member" (
  organization_id integer NOT NULL REFERENCES "pg-drizzle_organization"(id) ON DELETE CASCADE,
  user_id varchar(255) NOT NULL REFERENCES "user"(id) ON DELETE CASCADE,
  org_role varchar(32) NOT NULL DEFAULT 'member',
  created_at timestamptz NOT NULL DEFAULT now(),
  PRIMARY KEY (organization_id, user_id)
);
CREATE INDEX IF NOT EXISTS organization_member_user_idx ON "pg-drizzle_organization_member" (user_id);

CREATE TABLE IF NOT EXISTS "pg-drizzle_organization_group" (
  id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  organization_id integer NOT NULL REFERENCES "pg-drizzle_organization"(id) ON DELETE CASCADE,
  name varchar(256) NOT NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz
);
CREATE INDEX IF NOT EXISTS organization_group_org_idx ON "pg-drizzle_organization_group" (organization_id);

CREATE TABLE IF NOT EXISTS "pg-drizzle_group_member" (
  group_id integer NOT NULL REFERENCES "pg-drizzle_organization_group"(id) ON DELETE CASCADE,
  user_id varchar(255) NOT NULL REFERENCES "user"(id) ON DELETE CASCADE,
  created_at timestamptz NOT NULL DEFAULT now(),
  PRIMARY KEY (group_id, user_id)
);
CREATE INDEX IF NOT EXISTS group_member_user_idx ON "pg-drizzle_group_member" (user_id);

