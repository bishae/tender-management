CREATE TABLE IF NOT EXISTS "pg-drizzle_tender_approval_step" (
  id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  tender_id integer NOT NULL REFERENCES "pg-drizzle_tender"(id) ON DELETE CASCADE,
  group_id integer NOT NULL REFERENCES "pg-drizzle_organization_group"(id),
  step_order integer NOT NULL,
  status varchar(32) NOT NULL DEFAULT 'pending',
  acted_by_id varchar(255) REFERENCES "user"(id),
  acted_at timestamptz,
  note text,
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT tender_approval_step_order_unique UNIQUE (tender_id, step_order)
